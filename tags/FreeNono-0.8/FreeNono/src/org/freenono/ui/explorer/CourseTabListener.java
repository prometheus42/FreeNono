package org.freenono.ui.explorer;

import java.util.EventListener;

/**
 * Listener interface with one abstract method to inform components of changes
 * to a course tab button.
 */
public abstract class CourseTabListener implements EventListener {

    /**
     * Method that is called on every listener when a course tab button is
     * selected.
     */
    public abstract void courseTabChanged();
}
